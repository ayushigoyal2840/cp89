class Solution{
  public:
    TNode* sortedListToBST(LNode *head) {
        return to_bst(head);
    }
    
    TNode *to_bst(LNode *head) {
        if(not head) return nullptr;
        if(!head->next)
            return new TNode(head->data);
        
        LNode *mid_pre = getMiddle(head);
        
        TNode *root = new TNode(mid_pre->next->data);
        root->right = to_bst(mid_pre->next->next);
        mid_pre->next = nullptr;
        root->left = to_bst(head);
        
        return root;
    }
    
    LNode *getMiddle(LNode *head) {
        LNode *f = head, *s = head, *p = nullptr;
        while(f and f->next) {
            p = s;
            s = s->next;
            f = f->next->next;
        }
        // p stores the previous node of the middle node
        return p;
    }
};

